package com.autoya.autoya_api.autoya.domain.model.valueobjects;

/**
 * Enumeration representing different status types for car rentals.
 * These status types include WAITING, CONFIRMED, REQUIRED, and CANCELLED.
 * Each value in this enumeration represents a specific status of a car rental.
 */
public enum RentStatus {
    WAITING,  //en espera
    CONFIRMED,  //confirmado
    REQUIRED,   //solicitado
    CANCELLED   //cancelada
}
